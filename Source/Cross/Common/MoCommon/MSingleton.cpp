#include "MoCmSingleton.h"

MO_NAMESPACE_BEGIN

//============================================================
// <T>构造唯一对象的实例。</T>
//
// @return 实例
//============================================================
MSingleton::MSingleton(){
   _code = 0;
}

//============================================================
// <T>析构唯一对象的实例。</T>
//============================================================
MSingleton::~MSingleton(){
}

//============================================================
// <T>获得唯一对象的类型。</T>
//
// @return 类型
//============================================================
ESingleton MSingleton::SingletonType(){
   return ESingleton_Object;
}

//============================================================
// <T>获得唯一对象的代码。</T>
//
// @return 代码
//============================================================
TInt MSingleton::Code(){
   return _code;
}

//============================================================
// <T>获得唯一对象的名称。</T>
//
// @return 名称
//============================================================
TCharC* MSingleton::Name(){
   return _name.MemoryC();
}

//============================================================
// <T>构造处理。</T>
//
// @return 处理结果
//============================================================
TResult MSingleton::Construct(){
   return ESuccess;
}

//============================================================
// <T>析构处理。</T>
//
// @return 处理结果
//============================================================
TResult MSingleton::Dispose(){
   return ESuccess;
}

